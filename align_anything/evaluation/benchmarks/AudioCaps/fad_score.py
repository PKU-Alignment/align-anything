from frechet_audio_distance import FrechetAudioDistance
import argparse
import json

def parse_arguments():
    parser = argparse.ArgumentParser()
    parser.add_argument('--datasetpath', type=str, required=True, help='Path to the directory containing the original audio files.')
    parser.add_argument('--logs', type=str, required=True, help='Path to the directory containing the audio files generated by the model.')
    parser.add_argument('--name', type=str, required=True, help='Path where the FAD score should be saved.')
    return parser.parse_args()

def get_fad_score(datasetpath, logs, output_dir):
    frechet = FrechetAudioDistance(
        model_name="vggish",
        sample_rate=16000,
        use_pca=False,
        use_activation=False,
        verbose=False
    )
    fad_score = frechet.score(
        background_dir=datasetpath.strip("'"),
        eval_dir=logs.strip("'"),
        dtype="float32"
    )
    
    fad_score = float(max(fad_score, 0))
    FADScore = {
        "fad_score": fad_score
    }

    with open(output_dir.strip("'"), 'w') as file:
        json.dump(FADScore, file, indent=4)

def main():
    args = parse_arguments()
    get_fad_score(args.datasetpath, args.logs, args.name)

if __name__ == '__main__':
    main()